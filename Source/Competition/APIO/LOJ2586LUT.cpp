std::mt19937_64
    eng(std::chrono::high_resolution_clock::now()
            .time_since_epoch()
            .count());
std::uniform_int_distribution<int> uid(0, 399);
int build(int l, int r) {
    int m = (l + r) >> 1;
    int axis = uid(eng);
    Int64 bx = LUT[axis][0], by = LUT[axis][1];
    std::nth_element(
        C + l, C + m, C + r + 1,
        [=](const Circle& a, const Circle& b) {
            return bx * a.x + by * a.y <
                bx * b.x + by * b.y;
        });
    T[m].x[0] = C[m].x - C[m].r;
    T[m].x[1] = C[m].x + C[m].r;
    T[m].y[0] = C[m].y - C[m].r;
    T[m].y[1] = C[m].y + C[m].r;
    T[m].id = C[m].id;
    T[m].siz = r - l + 1;
    if(l < m) {
        T[m].l = build(l, m - 1);
        T[m].update(T[T[m].l]);
    }
    if(m < r) {
        T[m].r = build(m + 1, r);
        T[m].update(T[T[m].r]);
    }
    return m;
}
int did[size];
#define ls T[u].l
#define rs T[u].r
void modify(int u, const Circle& X) {
    if(u == 0 || T[u].x[0] > T[u].x[1] ||
       !T[u].intersect(X))
        return;
    int cid = T[u].id;
    if(!did[cid] && C[u].intersect(X))
        did[cid] = X.id;
    modify(ls, X);
    modify(rs, X);
    T[u].x[0] = std::min(T[ls].x[0], T[rs].x[0]);
    T[u].x[1] = std::max(T[ls].x[1], T[rs].x[1]);
    T[u].y[0] = std::min(T[ls].y[0], T[rs].y[0]);
    T[u].y[1] = std::max(T[ls].y[1], T[rs].y[1]);
    if(!did[cid]) {
        T[u].x[0] =
            std::min(T[u].x[0], C[u].x - C[u].r);
        T[u].x[1] =
            std::max(T[u].x[1], C[u].x + C[u].r);
        T[u].y[0] =
            std::min(T[u].y[0], C[u].y - C[u].r);
        T[u].y[1] =
            std::max(T[u].y[1], C[u].y + C[u].r);
    }
}
int rk[size];
bool cmpR(int a, int b) {
    return C[a].r == C[b].r ? C[a].id < C[b].id :
                              C[a].r > C[b].r;
}
const int inf = 1 << 30;
int main() {
    T[0].x[0] = T[0].y[0] = inf;
    T[0].x[1] = T[0].y[1] = -inf;
    IO::init();
    int n = read();
    for(int i = 1; i <= n; ++i) {
        C[i].id = i;
        C[i].x = read();
        C[i].y = read();
        C[i].r = read();
    }
    int rt = build(1, n);
    for(int i = 1; i <= n; ++i)
        rk[i] = i;
    std::sort(rk + 1, rk + n + 1, cmpR);
    for(int i = 1; i <= n; ++i) {
        int u = rk[i];
        if(!did[C[u].id])
            modify(rt, C[u]);
    }
    for(int i = 1; i <= n; ++i) {
        write(did[i]);
        IO::putc(' ');
    }
    IO::uninit();
    return 0;
}
